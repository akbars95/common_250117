  PROCEDURE INSERT_PASSWORD_HISTORY(PASSWORD_HISTORY_IN T_PASSWORD_HISTORY%ROWTYPE)
  AS
  BEGIN
    INSERT INTO T_PASSWORD_HISTORY(ACCOUNT_USER_ID, ACCOUNT_PASSWORD, AC_PAS_CHANGE_DATE_TIME)
    VALUES(PASSWORD_HISTORY_IN.ACCOUNT_USER_ID, PASSWORD_HISTORY_IN.ACCOUNT_PASSWORD, PASSWORD_HISTORY_IN.AC_PAS_CHANGE_DATE_TIME);
    COMMIT;
  END INSERT_PASSWORD_HISTORY;

