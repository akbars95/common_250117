  AS
    RETURN_RESULT T_PASSWORD_HISTORY%ROWTYPE;
  BEGIN
    RETURN_RESULT.ACCOUNT_USER_ID := ACCOUNT_USER_ID_IN;
    RETURN_RESULT.ACCOUNT_PASSWORD := ACCOUNT_PASSWORD_IN;
    RETURN_RESULT.AC_PAS_CHANGE_DATE_TIME := AC_PAS_CHANGE_DATE_TIME_IN;

    RETURN RETURN_RESULT;
  END GET_PASSWORD_HISTORY;

  FUNCTION GET_PASSWORD_HISTORY_BY_ACC_ID(ACCOUNT_USER_ID_IN T_PASSWORD_HISTORY.ACCOUNT_USER_ID%TYPE)
    RETURN T_PASSWORD_HISTORY%ROWTYPE
  AS
    RETURN_RESULT T_PASSWORD_HISTORY%ROWTYPE;
