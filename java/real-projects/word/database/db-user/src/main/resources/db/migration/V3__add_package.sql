            ACCOUNT_PASSWORD_IN IN T_PASSWORD_HISTORY.ACCOUNT_PASSWORD%TYPE,
            AC_PAS_CHANGE_DATE_TIME_IN IN T_PASSWORD_HISTORY.AC_PAS_CHANGE_DATE_TIME%TYPE)
